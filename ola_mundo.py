# Instruções de Entrada e Saída

# input() - instrução de entrada - capturar uma informação do teclado
# em formato de texto
nome = input("Digite seu nome: ")
idade = int(input("Digite sua idade: "))  # numeros inteiros
peso = float(input("Digite seu peso: "))  # numeros reais

# print() - instrução de saída - imprimir um texto na tela
print(f"Olá, {nome}! Seja bem vindo!!")
print("Seu peso é ", peso)
print("Sua idade é", idade)
print(f"Seu peso é {peso} e sua idade é {idade}")


