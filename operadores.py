# Operadores Relacionais
x = 5
y = 8

igual = (x == y)
diferente = (x != y)
menor_que = (x < y)
maior_que = (x > y)
menor_ou_igual = (x <= y)
maior_ou_igual = (x >= y)

print(igual, diferente, menor_que, maior_que, menor_ou_igual, maior_ou_igual)

# Operadores Lógicos
p = True
q = False

# Operador E
resultado_and = p and q

# Operador OU
resultado_or = p or q

# Operador Não
resultado_not = not p

print(resultado_and, resultado_or, resultado_not)
